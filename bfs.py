from collections import deque


def BFS(G, vertices, s):

    # -------------------------------------------------
    # Data: color[V], prev[V], d[V]
    # -------------------------------------------------

    color = {}
    prev = {}
    d = {}

    # -------------------------------------------------
    # for each vertex u ∈ V-{s}
    # {
    #     color[u] = WHITE
    #     prev[u] = NIL
    #     d[u] = inf
    # }
    # -------------------------------------------------

    for u in vertices:

        color[u] = "WHITE"
        prev[u] = None
        d[u] = float('inf')

    # -------------------------------------------------
    # color[s] = GRAY
    # d[s] = 0
    # prev[s] = NIL
    # -------------------------------------------------

    color[s] = "GRAY"
    d[s] = 0
    prev[s] = None

    # -------------------------------------------------
    # Q = empty
    # ENQUEUE(Q, s)
    # -------------------------------------------------

    Q = deque()

    Q.append(s)

    print("BFS Traversal Order:")
    print("--------------------")

    # -------------------------------------------------
    # While(Q not empty)
    # -------------------------------------------------

    while len(Q) != 0:

        # -------------------------------------------------
        # u = DEQUEUE(Q)
        # -------------------------------------------------

        u = Q.popleft()

        print(f"\nDequeued Vertex = {u}")
        print("Current Queue =", list(Q))

        # -------------------------------------------------
        # for each v ∈ adj[u]
        # -------------------------------------------------

        u_index = vertices.index(u)

        for v_index in range(len(vertices)):

            # If edge exists
            if G[u_index][v_index] == 1:

                v = vertices[v_index]

                # -----------------------------------------
                # if color[v] == WHITE
                # -----------------------------------------

                if color[v] == "WHITE":

                    # -----------------------------------------
                    # color[v] = GREY
                    # -----------------------------------------

                    color[v] = "GRAY"

                    # -----------------------------------------
                    # d[v] = d[u] + 1
                    # -----------------------------------------

                    d[v] = d[u] + 1

                    # -----------------------------------------
                    # prev[v] = u
                    # -----------------------------------------

                    prev[v] = u

                    # -----------------------------------------
                    # ENQUEUE(Q, v)
                    # -----------------------------------------

                    Q.append(v)

                    print(
                        f"Discovered {v} | "
                        f"d={d[v]} | "
                        f"prev={prev[v]}"
                    )

        # -------------------------------------------------
        # color[u] = BLACK
        # -------------------------------------------------

        color[u] = "BLACK"

        print(f"{u} is fully explored (BLACK)")

    # -------------------------------------------------
    # Final Result
    # -------------------------------------------------

    print("\n\nFINAL TABLE")
    print("------------------------------------------------------")
    print("Vertex\tColor\tDistance(d)\tPrev")

    for vertex in vertices:

        print(
            f"{vertex}\t"
            f"{color[vertex]}\t"
            f"{d[vertex]}\t\t"
            f"{prev[vertex]}"
        )


